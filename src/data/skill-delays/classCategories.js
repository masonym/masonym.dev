// helper to get job advancement tier from job code within a class
export function getJobTier(jobCode, className) {
  // check if it's a beginner job code (tier 0)
  if (BEGINNER_JOB_CODES.has(jobCode)) {
    return 0;
  }

  const codes = JOB_CODES[className];
  if (!codes) return null;

  const index = codes.indexOf(jobCode);
  if (index === -1) {
    return null;
  }

  // index directly maps to tier:
  // index 0 = beginner (tier 0), index 1 = 1st job (tier 1), etc.
  return index;
}

