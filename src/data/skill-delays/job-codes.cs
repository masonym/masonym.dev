        // derive job code from skill ID (first 3-5 digits depending on skill ID length)
        public static int GetJobCodeFromSkillId(string skillId)
        {
            if (string.IsNullOrEmpty(skillId) || skillId.Length < 7)
                return 0;
            
            // skill ID format: JJJJSSSS or JJJSSSS where J = job code digits, S = skill number
            // job code is skillId / 10000
            if (int.TryParse(skillId, out int id))
            {
                return id / 10000;
            }
            return 0;
        }

        // find which "main job" a job code belongs to
        public static string? GetMainJobName(int jobCode)
        {
            foreach (var kvp in JobNameToJobCodes)
            {
                foreach (int code in kvp.Value)
                {
                    if (code == jobCode)
                        return kvp.Key;
                }
            }
            return null;
        }
